#!/usr/bin/env node
/**
 * Reads data/ShortCuts/BankSMS.js and writes tests/.generated/bank-sms-lib.cjs
 * from the block between // @jest-extract-begin and // @jest-extract-end.
 * Run via npm pretest so Jest can require() pure helpers without loading top-level await.
 */
const fs = require("fs");
const path = require("path");

const root = path.join(__dirname, "..");
const src = path.join(root, "data", "ShortCuts", "BankSMS.js");
const outDir = path.join(root, "tests", ".generated");
const outFile = path.join(outDir, "bank-sms-lib.cjs");

const BEGIN = "// BEGIN_BANK_SMS_LIB_FOR_JEST";
const END = "// END_BANK_SMS_LIB_FOR_JEST";

const raw = fs.readFileSync(src, "utf8");
const i0 = raw.indexOf(BEGIN);
const i1 = raw.indexOf(END);
if (i0 === -1 || i1 === -1 || i1 <= i0) {
  console.error(
    "extract-bank-sms-lib-for-jest: markers missing or invalid in BankSMS.js",
  );
  process.exit(1);
}

const body = raw
  .slice(i0 + BEGIN.length, i1)
  .replace(/^\r?\n/, "")
  .trimEnd();

const out = `/* eslint-disable */\n/* AUTO-GENERATED by scripts/extract-bank-sms-lib-for-jest.js — do not edit */\n\n${body}\n\nmodule.exports = {\n  extractTime,\n  splitMessages,\n  reassembleMessages,\n  KEYWORDS,\n  MONEY_RE,\n  SPAM_RE,\n  DATE_ONLY_RE,\n  fmt,\n};\n`;

fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(outFile, out, "utf8");
console.log("Wrote", path.relative(root, outFile));

const metaFile = path.join(outDir, "extract-meta.json");
fs.writeFileSync(
  metaFile,
  JSON.stringify(
    {
      source: path.relative(root, src),
      output: path.relative(root, outFile),
      generatedAt: new Date().toISOString(),
      extractedChars: body.length,
    },
    null,
    2,
  ),
  "utf8",
);
console.log("Wrote", path.relative(root, metaFile));
